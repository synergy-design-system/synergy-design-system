/**
 * Generates a config file based on the provided options
 * @param {string[]} baseline The baseline to generate the config for
 * @param {import('../types.js').CreateBaselineOptions['version']} version The version to generate the config for
 * @param {string?} baselineQuery The original baseline query used to generate the baseline (for documentation purposes)
 * @returns {string} The generated config file content
 */
export const generateConfig = (
  baseline,
  version,
  baselineQuery,
) => {
  const json = JSON.stringify(baseline, null, 2);
  const fileContent = `
/**
 * Baseline for synergy version ${version}
 *
 * @version ${version}
 * @since ${new Date().toISOString()}
 *
 * Created with the following baseline query:
 * ${baselineQuery || 'N/A'}
 * 
 * This file is auto-generated by the createBaseline function. Do not edit this file directly.
 * To update the baseline for this version of synergy, run the following command:
 * node src/syn-create-baseline.js --baseline "${baselineQuery || 'N/A'}" --version ${version} --allow-replace
 */
export default ${json};
`.trimStart();

  return fileContent;
};
